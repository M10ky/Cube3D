#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	char	*src;
	size_t	size;
	int		i;

	src = (char *)s;
	size = ft_strlen(src) + 1;
	i = 0;
	dest = malloc(size * sizeof(char));
	if (dest == NULL)
		return (NULL);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
