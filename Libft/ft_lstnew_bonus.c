#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new_content;

	new_content = malloc(sizeof(t_list));
	if (new_content == NULL)
		return (NULL);
	new_content -> content = content;
	new_content -> next = NULL;
	return (new_content);
}
